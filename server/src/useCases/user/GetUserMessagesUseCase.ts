import { IGetUserMessagesUseCase } from "../../entities/useCasesInterfaces/user/IGetUserMessagesUseCase";
import { IUserRepository } from "../../interfaceAdapters/repositories/user/IUserRepository";


export class GetUserMessagesUseCase implements IGetUserMessagesUseCase{
    constructor(private iuserrepository: IUserRepository){};
 async GetUserMessagesUse(adminId: string): Promise<any> {
    try {
        const getMessages = await this.iuserrepository.FindAndGetUserMessages(adminId);
        return getMessages;
    } catch (error) {
        return error;
    }
}
}






























































































































































































































































































































































































































































